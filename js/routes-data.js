/**
 * Retorna todas as capitais disponíveis
 * @returns {Array} - Array com todas as capitais
 */
function getAllCapitals() {
    return CAPITALS;
}

/**
 * Retorna o índice de uma capital no array
 * @param {string} capitalName - Nome da capital
 * @returns {number} - Índice da capital ou -1 se não encontrada
 */
function getCapitalIndex(capitalName) {
    return CAPITALS.indexOf(capitalName);
}

/**
 * Calcula a distância entre duas capitais
 * @param {string} origin - Nome da capital de origem
 * @param {string} destination - Nome da capital de destino
 * @returns {number|null} - Distância em km ou null se inválida
 */
function getDistanceBetweenCapitals(origin, destination) {
    if (!origin || !destination) {
        return null;
    }
    
    if (origin === destination) {
        return 0;
    }
    
    const originIndex = getCapitalIndex(origin);
    const destinationIndex = getCapitalIndex(destination);
    
    if (originIndex === -1 || destinationIndex === -1) {
        return null;
    }
    
    return DISTANCES[originIndex][destinationIndex];
}

/**
 * Formata o nome da rota para exibição
 * @param {string} origin - Capital de origem
 * @param {string} destination - Capital de destino
 * @returns {string} - String formatada (ex: "São Paulo → Rio de Janeiro")
 */
function formatRouteName(origin, destination) {
    if (!origin || !destination) {
        return '';
    }
    return `${origin} → ${destination}`;
}
